	public class Rect {

		int x;
		int y;
		int w;
		int h;
		Color c;

		//player constructor
		Rect(int x, int y, int w, int h, Color c){

			this.x = x;
			this.y = y;
			this.w = w;
			this.h = h;
			this.c = c;

		}

		//former player constructor
		Rect(Rect rect){

			this.x = rect.x;
			this.y = rect.y;
			this.w = rect.w;
			this.h = rect.h;
			this.c = rect.c;

		}
		
		//wall player constructor
		Rect(int x, int y){
			this.x = x;
			this.y = y;
			w = 10;
			h = 10;
			c = Color.white;
		}

		public boolean collision(Rect target) {
			if(x < target.x +target.w && 
					x+w > target.x &&
					y < target.y +target.h &&
					y+h > target.y) {
				return true;
			}

			return false;
		}

	}
	
	public void startTime() {

		if (timer == null) {
            timer = new Timer(1000, new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    tiempoTranscurrido++;
                    actualizarLabel();
                }
            });
            timer.start();
        }
		
	}

	public void stopTime() {

		if (timer != null) {
            timer.stop();
            timer = null;
        }
		
	}

	public void restartTime() {

		tiempoTranscurrido = 0;
        actualizarLabel();
        if (timer != null) {
            timer.stop();
            timer = null;
        }
		
	}
	
	private void actualizarLabel() {
        int minutos = (tiempoTranscurrido % 3600) / 60;
        int segundos = tiempoTranscurrido % 60;
        String tiempo = String.format("%02d:%02d", minutos, segundos);
        time.setText(tiempo);
    }
